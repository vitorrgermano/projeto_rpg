var escolha;

function pas_1() {
    while (true) {
        escolha = prompt("Qual sua escolha? \n- Seguirei minha jornada \n- Ficarei no meu feudo");
        if (escolha.toLowerCase() == "a" ) {
            part1.style.display = "none";
            part2_boa.style.display = "block";
            if (escolha.toLowerCase)
            break;
        } else if (escolha.toLowerCase() == "ficarei no meu feudo") {
            part1.style.display = "none";
            part2_ruim.style.display = "block";
            break;
        } else {
            alert("Insira uma opção válida")
        }
    }
}

